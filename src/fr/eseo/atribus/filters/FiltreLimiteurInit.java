package fr.eseo.atribus.filters;

import javax.servlet.FilterConfig;
import javax.servlet.ServletException;

/**
 * Le filtre FiltreLimiteurInit ayant pour but d'initialiser des filtres.
 */
public class FiltreLimiteurInit {

  /**
   * Destruction du filtre.
   */
  public void destroy() {
    return;
  }

  /**
   * Initialise le filtre.
   *
   * @param arg0 le arg0
   * @throws ServletException de type servlet exception
   */
  public void init(FilterConfig arg0) throws ServletException {
    final FilterConfig tmp = arg0;
    tmp.getClass();
    return;
  }
}
